package a0317;

public class while1_2 {
    public static void main(String[] args) {
        int count = 0;
        int count1 = 0;

        while(count <3 ){  //3까지 구문이돌고 4에서 멈춤
            count++;
            System.out.println("현재의 숫자는" + count);
        }
    

        while(count1 <=3 ){  //4까지 구문이돌고 멈춤
            count1++;
            System.out.println("현재의 숫자는" + count1);
        }
    }
}
